package Divide_and_Conquer_Algorithms;

import java.util.Scanner;

public class MergeSort
{
    static void mergeSort(int[] arr, int l, int r)
    {
        if (l < r)
        {
            int m = (l + r) / 2;
            mergeSort(arr, l, m);
            mergeSort(arr, m + 1, r);
            merge(arr, l, m, r);
        }
    }

    static void merge(int[] arr, int l, int m, int r)
    {
        int n1 = m - l + 1;
        int n2 = r - m;
        int[] L = new int[n1];
        int[] R = new int[n2];
        for (int i = 0; i < n1; i++)
            L[i] = arr[l + i];
        for (int j = 0; j < n2; j++)
            R[j] = arr[m + 1 + j];
        int i = 0, j = 0;
        int k = l;
        while (i < n1 && j < n2)
        {
            if (L[i] <= R[j])
            {
                arr[k] = L[i];
                i++;
            }
            else
            {
                arr[k] = R[j];
                j++;
            }
            k++;
        }
        while (i < n1)
        {
            arr[k] = L[i];
            i++;
            k++;
        }
        while (j < n2)
        {
            arr[k] = R[j];
            j++;
            k++;
        }
    }

    public static void main(String[] args)
    {
     // Get array from user
     Scanner sc = new Scanner(System.in);
     System.out.println("Enter the number of elements in the array: ");
     int n = sc.nextInt();
     int[] arr = new int[n];
     System.out.println("Enter the elements of the array: ");
     for (int i = 0; i < n; i++)
         arr[i] = sc.nextInt();

     // Sort the array
     mergeSort(arr, 0, n - 1);

     // Print the sorted array
     System.out.println("The sorted array is: ");
     for (int i = 0; i < n; i++)
         System.out.print(arr[i] + " ");
     System.out.println();

    }
}
